  }

  Color skinColor(WBColorKey key) {
    Color color;
    switch (_currentSkinStyle) {
      case skinStyleLight:
        color = lightColors[key.index];
        break;
      case skinStyleDark:
        color = darkColors[key.index];
        break;
      default:
    }
    return color;
  }

